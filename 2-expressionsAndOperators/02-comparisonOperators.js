// Equality

    // equal operator
    '3' == 3;  // -> true

    // strict equality operator
    '3' === 3; // -> false

// Opposite operator (NOT/bang operator/exclamation)
    '3' != 3; // -> false

    '3' !== 3; // -> true

// Greater than (or equal to)
    3 > 4; // -> false
    3 >= 4; // -> false

// Lesser than (or equal to)
    3 < 4; // -> true
    3 <= 4; // -> true

// AND operator
   let x = 13;

   x >= 5 && x <= 33;  // -> true (b/c both sides are true)

// OR operator
    x > 3 || x == -3;
